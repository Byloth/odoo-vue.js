var FieldFontIconPicker = common.AbstractField.extend(common.ReinitializeFieldMixin, {
    template: 'FieldFontIconPicker',
    events: {
        'change': 'store_dom_value',
    },
    initialize_content: function() {
        if(!this.get('effective_readonly') && !this.$picker) {
            this.$picker = this.$el.find('.iconpicker-component');
        }
        this.setupFocus(this.$el);
    },
    destroy_content: function() {
        this.$picker = undefined;
    },
    store_dom_value: function() {
        if (this.$picker)
            this.internal_set_value(this.iconpicker.iconpickerValue);
    },
    commit_value: function () {
        this.store_dom_value();
        return this._super();
    },
    render_value: function () {
        let self = this;
        let value = this.get('value');
        if (!this.get("effective_readonly")) {

            this.iconpicker = this.$picker.iconpicker({
                selected: value,
                icons: fa45,
                fullClassFormatter: function(val) {
                    return 'fa fa-fw ' + val;
                },
                mustAccept: true,
                showFooter: true,
                templates: {
                    buttons:
                        '<button class="iconpicker-btn iconpicker-btn-cancel btn btn-default btn-sm" title="">Cancel</button>'+
                        '<button class="iconpicker-btn iconpicker-btn-accept btn btn-primary btn-sm" title="">Accept</button>'+
                        '<button class="iconpicker-btn iconpicker-btn-clear btn btn-warning btn-sm" title=""><b class="fa fa-trash"></b></button>',
                    search: '<input type="search" class="form-control iconpicker-search" placeholder="Type to filter" />',
                }
            }).data('iconpicker');

            // INFO: bootstrap 3.3.4 closes dropdown when clicked on search input field.
            //       Here is workaround to fix it.
            if ($.fn.dropdown.Constructor.VERSION < '3.3.5') {
                this.$el.on({
                    "click": function(event) {
                        if ($(event.target).closest('.dropdown-toggle').length) {
                            $(this).data('closable', true);
                        } else {
                            $(this).data('closable', false);
                        }
                    },
                    "hide.bs.dropdown": function(event) {
                        let hide = $(this).data('closable');
                        $(this).data('closable', true);
                        return hide;
                    }
                });
                this.$el.on('click', '.iconpicker-btn', function () {
                   $(this).parents('.dropdown').removeClass('open');
                });
            }

            // INFO: pressing the cancel button (to fix cancel issue with fontawesome-iconpicker when dropdown).
            this.$el.on('click', '.iconpicker-btn-cancel', function () {
                // INFO: reset to original value when pressed cancel button.
                // let iconpicker = $(this).parents('.iconpicker-dropdown').find('.iconpicker-component').data('iconpicker');
                self.iconpicker.setValue(self.get('value'));
                // INFO: refresh widget.
                self.iconpicker._updateComponents();
            });
            // INFO: pressing the clear button.
            this.$el.on('click', '.iconpicker-btn-clear', function () {
                // INFO: invalidates iconpickerValue (there is no need to reset data-iconpickervalue).
                // var iconpicker = $(this).parents('.iconpicker-dropdown').find('.iconpicker-component').data('iconpicker');
                self.iconpicker.setValue(false);
                // INFO: refresh widget.
                self.iconpicker._updateComponents();
            });
        }
        else {
            // INFO: read only widget.
            this.$el.attr('title', value);
            this.$el.find('i').attr('class', 'fa fa-fw ' + value);
        }
    },
    is_false: function() {
        return this.get('value') === '' || this._super();
    },
    focus: function() {
        if (this.$picker) {
            return this.$picker.focus();
        }
        return false;
    },
});

core.form_widget_registry.add('fonticonpicker', FieldFontIconPicker);

return {
    FieldFontIconPicker: FieldFontIconPicker
};
